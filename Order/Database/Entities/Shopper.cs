using System;

/// <summary>
/// Класс покупателя.
/// </summary>
public class Shopper
{
    /// <summary>
    /// покупателя.
    /// </summary>
    public int Id { get;}
    /// <summary>
    /// Имя покупателя.
    /// </summary>
	public string Name { get; set; }
    /// <summary>
    /// Фамилия покупателя.
    /// </summary>
    public string Surname { get; set; }
    /// <summary>
    /// Адресс покупателя.
    /// </summary>
    public string Address { get; set; }
    /// <summary>
    /// Номер телефона покупателя.
    /// </summary>
    public string PhoneNumber { get; set; }
    /// <summary>
    /// Почта покупателя.
    /// </summary>
    public string Mail { get; set; }
}
